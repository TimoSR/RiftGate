namespace Domain.Enums;

public enum UserRole
{
    Participant,   // General term for players or users participating in the virtual economy
    Developer,     // Developers or creators of virtual content
    Analyst,       // Users focused on analytics and data within the virtual economy
    ContentCreator,// Users who create and contribute content or items for the game
    Moderator,     // Users who moderate the virtual environment and user interactions
    Administrator, // Administrators with full access to the system
    Auditor,       // Users who audit transactions and activities for compliance
    Trader,        // Users who are actively involved in trading virtual items or currencies
    Investor       // Users who invest in virtual assets or economies within the platform
}